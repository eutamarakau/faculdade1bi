#programa que mostra todas as operações aritmeticas entre x e y 

x = 4
y = 8

print('A soma de x e y é =', x + y)
print('A subtração de x e y é =', x - y)
print('A multiplicação de x e y é =', x * y)
print('A divisão de x e y é =', x / y)
print('A divisão inteira de x e y é =', x // y)
print('O resto da divisão de x e y é =', x % y)
print('A exponenciação de x e y é =', x ** y)
