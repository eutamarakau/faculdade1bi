#script com duas variaveis com valores inteiros 

x = 99
y = 14

print("{} == {}: {}".format(x,y, x == y))
print("{} != {}: {}".format(x,y, x != y))
print("{} < {}: {}".format(x,y, x < y))
print("{} <= {}: {}".format(x,y, x <= y))
print("{} > {}: {}".format(x,y, x > y))
print("{} >= {}: {}".format(x,y, x >= y))
print("{} and {}: {}".format(x,y, x and y))
print("{} or {}: {}".format(x,y, x or y))
print("{} not {}: {}".format (x,y, not( x != y)))