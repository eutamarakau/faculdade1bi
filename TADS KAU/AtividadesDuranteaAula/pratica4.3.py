# Objetivo: Determinar se um numero é positivo, negativo ou neutro

x = int(input("Digite um número: "))

#deteminar se é positivo ou negativo
if x > 0 :
  print(f'{x} é possitivo')
elif x < 0:
  print(f"{x} é negativo")
else:
  print(f"{x} é neutro")  






























